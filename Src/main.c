int main(void)
{

  /* USER CODE BEGIN 1 */
	char str0[100];
	char str1[100];
	char * currentline;
	TM_MPU6050_t MPU6050_Data0;

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();

  /* USER CODE BEGIN 2 */
	while(HAL_GetTick()<1000);
  HAL_UART_Transmit(&huart1,(uint8_t*)"\0AT",3,50000);
	while(HAL_GetTick()<2000);
	TM_MPU6050_Init(&MPU6050_Data0, TM_MPU6050_Device_0, TM_MPU6050_Accelerometer_8G, TM_MPU6050_Gyroscope_500s);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	currentline = str0;
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
                /* Read all data from sensor 1 */
                TM_MPU6050_ReadAll(&MPU6050_Data0);
                
                /* Format data */
		sprintf(currentline, "%+6d;%+6d;%+6d;%+6d;%+6d;%+6d\r\n",
                    MPU6050_Data0.Accelerometer_X,
                    MPU6050_Data0.Accelerometer_Y,
                    MPU6050_Data0.Accelerometer_Z,
                    MPU6050_Data0.Gyroscope_X,
                    MPU6050_Data0.Gyroscope_Y,
                    MPU6050_Data0.Gyroscope_Z
                );
                
                /* Show to usart */
		int len = strlen(currentline);
		while(huart1.hdmatx->State != HAL_DMA_STATE_READY) __wfi();
		UART_CheckIdleState(&huart1);
		HAL_UART_Transmit_DMA(&huart1,(uint8_t*)currentline,len);
		currentline = currentline == str0 ? str1 : str0;

  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  HAL_RCC_OscConfig(&RCC_OscInitStruct);

  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_SYSCLK;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_HSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0);

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_USART1|RCC_PERIPHCLK_I2C1;
  PeriphClkInit.Usart1ClockSelection = RCC_USART1CLKSOURCE_PCLK1;
  PeriphClkInit.I2c1ClockSelection = RCC_I2C1CLKSOURCE_HSI;
  HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit);

  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

